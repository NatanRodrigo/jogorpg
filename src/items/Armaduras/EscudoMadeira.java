package items.Armaduras;

public class EscudoMadeira {
        private String nome;
        private int protecao;

    public EscudoMadeira(String nome, int protecao) {
        this.nome ="Escudo De Madeira";
        this.protecao = 30;
    }

    public String getNome() {
        return nome;
    }

    public int getProtecao() {
        return protecao; }
}
